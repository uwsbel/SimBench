print("Example: PyChrono using Euler-Bernoulli beam finite elements") 


sys = chrono.ChSystemSMC()


mesh = fea.ChMesh()


msection = fea.ChBeamSectionEulerBernoulli()


beam_wy = 0.012
beam_wz = 0.025
msection.SetAsRectangularSection(beam_wy, beam_wz) 


msection.SetYoungModulus(0.01e9) 
msection.SetShearModulus(0.01e9 * 0.3) 
msection.SetRayleighDamping(0.000) 
msection.SetCentroid(0, 0.02) 
msection.SetShearCenter(0, 0.1) 
msection.SetSectionRotation(45 * chrono.CH_RAD_TO_DEG) 


beam_L = 0.1


hnode1 = fea.ChNodeFEAxyzrot(chrono.ChFramed(chrono.ChVector3d(0, 0, -0.1))) 
hnode2 = fea.ChNodeFEAxyzrot(chrono.ChFramed(chrono.ChVector3d(beam_L, 0, -0.1))) 
hnode3 = fea.ChNodeFEAxyzrot(chrono.ChFramed(chrono.ChVector3d(beam_L * 2, 0, -0.1))) 


mesh.AddNode(hnode1)
mesh.AddNode(hnode2)
mesh.AddNode(hnode3)


builder = fea.ChBuilderBeamEuler()
builder.SetSection(msection)
builder.SetDirection(chrono.ChVector3d(0, 1, 0)) 
builder.SetLength(beam_L)
builder.SetNumElements(5)
beam_section = builder.BuildBeam(hnode1, hnode3)


builder.GetLastBeamNodes().back().SetFixed(True)


hnode1.SetForce(chrono.ChVector3d(0, -1, 0))


belement1 = fea.ChElementBeamEuler()
belement1.SetNodes(hnode1, hnode2) 
belement1.SetSection(beam_section) 
mesh.AddElement(belement1) 


belement2 = fea.ChElementBeamEuler()
belement2.SetNodes(hnode2, hnode3) 
belement2.SetSection(beam_section) 
mesh.AddElement(belement2) 


hnode3.SetTorque(chrono.ChVector3d(0, -0.04, 0))


mtruss = chrono.ChBody()
mtruss.SetFixed(True) 
sys.Add(mtruss) 


